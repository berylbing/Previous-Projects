package com.ht706.componentLibrary.systemManagement.actionForm;

import org.apache.struts.action.ActionForm;

import com.ht706.componentLibrary.systemManagement.bean.hbm.Organization;
/**
 * 
 * @Document:
 * @author: zd
 * @Time: 2010-08-31 
 *
 */


public class OrganizationForm extends ActionForm{
	private Organization organization=new Organization();
	private String password;
	private String birthDay;
	private int [] selectedOrganization=new int[0];
	public void setOrganization(Organization organization) {
		this.organization = organization;
	}

	public Organization getOrganization() {
		return organization;
	}

	public void setSelectedOrganization(int [] selectedOrganization) {
		this.selectedOrganization = selectedOrganization;
	}

	public int [] getSelectedOrganization() {
		return selectedOrganization;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPassword() {
		return password;
	}

	public void setBirthDay(String birthDay) {
		this.birthDay = birthDay;
	}

	public String getBirthDay() {
		return birthDay;
	}




}
